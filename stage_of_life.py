age = 2
if age < 2:
	print("You're a baby.")
if (age >= 2) and (age < 4):
	print("You're a toddler.")
if (age >= 4) and (age < 13):
	print("You're a kid.")
if (age >= 13) and (age < 20):
	print("You're a teenager.")
if (age >= 20) and (age < 65):
	print("You're an adult.")
else:
	print("You're an elder.")